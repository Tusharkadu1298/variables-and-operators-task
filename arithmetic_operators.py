# 4. Apply all the types of Arithmetic Operators on the following Variables:
#   a = 40
#   b = 5


a = 40
b = 5
print(a + b)                # addition 
print(a - b)                # minus
print(a * b)                # multipy
print(a / b)                # divide
print(a // b)               # floor division
print(a % b)                # modulus
print(a ** 2)               # exponation
print(b ** 2)               # exponation
